#include <iostream>
using namespace std;
int main(){
    // int = 4 bytes 
    // 1 byte = 8 bits 
    int age = 10;
    cout<<"Age is : " << age << endl;

    // char = 1 bytes 
    char ch = 'A';
    cout<<"Character is : " << ch << endl;

    // float = 4 bytes 
    float pi = 3.14f;
    cout<<"Value of pi is : " << pi << endl;

    // bool = 1 bytes 
    // true = 1 , false = 0
    bool isAdult = true;
    cout<<"Is Adult : " << isAdult << endl;

    // double = 8 bytes 
    double largeDecimal = 3.141592653589793;
    cout<<"Value of large decimal is : " << largeDecimal << endl;
    return 0;

}