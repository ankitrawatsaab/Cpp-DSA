#include <iostream>
#include <vector>
using namespace std;

//using optimized prefix and suffix approach (space optimized)
vector <int> productArrayExceptSelf (vector <int> nums){
    int n = nums.size();
    vector <int> ans(n,1);

    //prefix --> ans array used for prefix
    for(int i = 1; i<n; i++){
        ans[i] = ans[i-1] * nums[i-1];
    }

    //suffix --> using a variable instead of suffix array
    int suffix = 1;
    for(int i= n-2; i>=0; i--){
        suffix = suffix * nums[i+1];
        ans[i] = ans[i] * suffix;
    }
    return ans;
}

int main() {
    vector <int> nums = {1,2,3,4};
    vector <int> result = productArrayExceptSelf(nums);
    for(int value : result){
        cout<<value << " ";
    }
    return 0;
}